// ── t() — main translation function ──────────────────────────────────────────
// Supports placeholders: t("dash.welcome", { name: "Ani" }) → "Welcome, Ani"
export function getLang() {
  try {
    const s = JSON.parse(localStorage.getItem("armexam_general_settings") || "{}");
    return s.language || "ru";
  } catch { return "ru"; }
}

export function t(key, vars) {
  const lang  = getLang();
  const dict  = TRANSLATIONS[lang] ?? TRANSLATIONS["ru"];
  let   str   = dict[key] ?? TRANSLATIONS["ru"][key] ?? key;
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => { str = str.replace(`{${k}}`, v); });
  }
  return str;
}
